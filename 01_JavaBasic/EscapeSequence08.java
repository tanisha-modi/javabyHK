
// BackSlash b  and BackSlash f

public class EscapeSequence08 {
    public static void main(String[] args){

        // \b
        System.out.println("abcdef\b");
        System.out.println("abcde\bf");
        System.out.println("abcde\b\bf");


        //  \f
        System.out.println("abc\fdef");
         
    }
}
